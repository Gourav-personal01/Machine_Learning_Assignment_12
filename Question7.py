# # Q7. How do you interpret the coefficients of Ridge Regression?

# In Ridge Regression, the magnitude of the coefficients is affected by the regularization term. Ridge Regression adds a penalty to the sum of squared coefficients in the objective function

# The sign (positive or negative) of the coefficients in Ridge Regression still indicates the direction of the relationship between a predictor and the target variable

# The interpretation of coefficients in terms of unit changes in predictors remains the same. For example, if the coefficient of a predictor is 0.5, it means that a one-unit increase in
# that predictor is associated with a 0.5-unit increase in the target variable, holding all other predictors constant.