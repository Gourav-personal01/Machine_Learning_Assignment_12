# Q2. What are the assumptions of Ridge Regression?

# Ridge Regression assumes that the relationship between the dependent variable (the target) and the independent variables (predictors) is linear.

# Ridge Regression assumes that the errors (residuals) from the model are independent of each other

# Ridge Regression assumes that there is no perfect multicollinearity among the independent variables.

# Ridge Regression assumes the availability of a regularization parameter (Î» or alpha) that controls the strength of regularization

